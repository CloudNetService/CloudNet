package eu.cloudnetservice.driver.network.rpc.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation can be used to declare the constructor that should be invoked when generating RPC api implementations
 * of the declaring class. If not present on any constructor in the class, then a no args constructor must be defined
 * which will be used instead.
 *
 * @since 4.0
 */
@Documented
@Target(ElementType.CONSTRUCTOR)
@Retention(RetentionPolicy.RUNTIME)
public @interface RPCInvocationTarget {

}
